◊hanging-topic[(topic-from-metas metas)]{Use straight quotes, not curly}

◊first-use{Foot and inch marks} — also known as minute and second marks or prime and double prime marks, depending on what they’re labeling — are not curly. Use straight quotes for these marks.

“But back in ◊xref{straight and curly quotes}, you told me to turn on smart quotes. So now, when I try to type foot and inch marks, they come out curly, not straight.” A fair point.

◊(omission)

Typography purists would point out that proper foot and inch marks have a slight northeast-to-southwest slope to them. True, but those characters aren’t available in many fonts. So straight quotes are the most reliable tools for the job.