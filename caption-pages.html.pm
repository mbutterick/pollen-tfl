If this book were a law-school class, reformatting a ◊em{caption page} could be the final exam. Caption pages pack a lot of typographic issues into a small area. How many can you spot?

◊(omission)

Recall the second ◊xref{maxim of page layout} — ◊em{divide the page into foreground and background}. The vertical rules on a caption page should seem like part of the background — they should not be darker or more prominent than the body text in the foreground. Removing unnecessary rules will make the body text area feel less cramped. Use as few as possible. In this revised version, I removed the vertical rule on the right and one on the left. I made the remaining rule thinner and moved it away from the text.

Court filings have ugly typography as a matter of habit, not requirement. Court rules can be strict, but there’s still plenty of room for good typography (see ◊xref{how to interpret court rules}).

